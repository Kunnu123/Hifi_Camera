	private final static int func_mod_list[] = {
			0x01,
			0x02,
			0x04,
			0x08,
			0x10,
			0x20,
			0x40,
			0x80
	};


	private HI_P2P_FUNCTION cmd_function = null;



	public void setCmdfunction(HI_P2P_FUNCTION cmd_function) {
		this.cmd_function = cmd_function;
	}

	public boolean getAppCmdFunction(int cmd) {

		boolean ret = false;
		if(cmd_function == null) {
			return false;
		}
		HiLog.v("getAppCmdFunction："+cmd);
		int i = 0;
		int index = 0;
		for(i=0;i<cmd_list.length;i++) {
			if(cmd_list[i] == cmd) {
				index = i;
				ret = true;
				break;
			}
		}
		int step = index / 8;
		int mod = index % 8;

		HiLog.v("getAppCmdFunction："+step+":::::"+mod+"::"+ret+"::"+index);
		HiLog.v("getAppCmdFunction："+cmd_function.s32Function[step]+":::::"+func_mod_list[mod]);
		if((cmd_function.s32Function[step] & func_mod_list[mod]) != 0 && ret) {
			return true;
		}

		return false;
	}


